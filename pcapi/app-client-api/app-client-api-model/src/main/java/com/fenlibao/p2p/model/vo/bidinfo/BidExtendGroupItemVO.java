package com.fenlibao.p2p.model.vo.bidinfo;

public class BidExtendGroupItemVO {
	
	public String itemKey;  //组每项的key值
	
	public String itemValue;  //组每项的value值
	
	public String itemCode;  //组每项的code值
	
	public String itemType;  //组每项的type值

	public String getItemKey() {
		return itemKey;
	}

	public void setItemKey(String itemKey) {
		this.itemKey = itemKey;
	}

	public String getItemValue() {
		return itemValue;
	}

	public void setItemValue(String itemValue) {
		this.itemValue = itemValue;
	}

	public String getItemCode() {
		return itemCode;
	}

	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}

	public String getItemType() {
		return itemType;
	}

	public void setItemType(String itemType) {
		this.itemType = itemType;
	}

}
